SELECT CATEGORY, SUM(t2.SALES) AS TOTAL_SALES FROM BOOK t1
JOIN BOOK_SALES t2 ON t1.BOOK_ID = t2.BOOK_ID
WHERE t1.BOOK_ID IN (
SELECT BOOK_ID FROM BOOK_SALES
WHERE SALES_DATE BETWEEN "2022-01-01" AND "2022-01-31"
GROUP BY BOOK_ID)
AND t2.SALES_DATE BETWEEN "2022-01-01" AND "2022-01-31"
GROUP BY CATEGORY
ORDER BY CATEGORY ASC;
